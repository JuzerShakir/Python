# Additon
# adds two numbers prints the value on screen
# the print function only prints the value on screen if it's in a string format
# if numbers are given without quotes then it treats it as a number, calculates,..
#....& displays the value on the screen without showing the calculation.
# same goes for very operations
#'\t' is in between the strings doesn't actually print this '\t' but gives a tab space
print("Additon \t 5 + 2 \t = ", 5 + 2)

# substraction
# substracts two numbers and prints the value on screen
print("Substraction \t 5 - 2 \t = ", 5 - 2)

# multiplication
# multiplies two or more numbers and displays the result
print("Multiplication \t 5 x 2 \t = ", 5 * 2)

# Division
# Divides two numbers and displays the result
# gives the quotient of the Division
print("Division \t 5 / 2 \t = ", 5 / 2)

# this '//' operator gives the quotient of the Division in the form of int
print("Division \t 5 // 2\t = ", 5 // 2)

# this '%' operator gives the remainder of the Division
print("Modulus \t 5 % 2\t = ", 5 % 2)

# Exponents
# double asteriks used to raise to the power of something
print("Exponent \t 5 ^ 2\t = ", 5 ** 2)

# order of operations
# calculates the result of the parenthesis first
print("\n((3 + 2) x 6) = ", ((3 + 2) * 6))
print("(((4 // 2) x 10) ^ 2) = ", (((4 // 2) * 10) ** 2))

